func (pc *policyController) getGpolTriggers(match *admissionregistrationv1.MatchResources) []*unstructured.Unstructured {
	var triggers []*unstructured.Unstructured
	objectSelector := match.ObjectSelector
	nsSelector := match.NamespaceSelector

	for _, rule := range match.ResourceRules {
		for _, group := range rule.APIGroups {
			for _, resource := range rule.Resources {
				versions := rule.APIVersions
				// RESTMapper does not support wildcard versions. Resolve all
				// concrete versions for the given group/resource before processing.
				if len(rule.APIVersions) == 1 && rule.APIVersions[0] == "*" {
					gvks, err := pc.restMapper.KindsFor(
						schema.GroupVersionResource{
							Group:    group,
							Resource: resource,
						},
					)
					if err != nil {
						pc.log.Error(err, "failed to resolve wildcard APIVersions", "group", group, "resource", resource)
						continue
					}
					versionSet := sets.New[string]()
					for _, gvk := range gvks {
						versionSet.Insert(gvk.Version)
					}
					versions = sets.List(versionSet)
				}
				for _, version := range versions {
					groupVersion := schema.GroupVersion{
						Group:   group,
						Version: version,
					}
					gvr := schema.GroupVersionResource{Group: group, Version: version, Resource: resource}
					gvk, err := pc.restMapper.KindFor(gvr)
					if err != nil {
						pc.log.Error(err, "mapping gvr to gvk failed", "gvr", gvr)
						continue
					}
					resources, err := pc.client.ListResource(context.TODO(), groupVersion.String(), gvk.Kind, "", objectSelector)
					if err != nil {
						pc.log.Error(err, "failed to list resources", "groupVersion", groupVersion, "kind", gvk.Kind)
						continue
					}
					for i, res := range resources.Items {
						if !pc.triggerMatches(res, gvr, rule.ResourceNames, match.ExcludeResourceRules, nsSelector) {
							continue
						}
						triggers = append(triggers, &resources.Items[i])
					}
				}
			}
		}
	}
	return triggers
}

func filterTriggersByNamespace(triggers []*unstructured.Unstructured, namespace string) []*unstructured.Unstructured {
	if namespace == "" {
		return triggers
	}
	filtered := make([]*unstructured.Unstructured, 0, len(triggers))
	for _, trigger := range triggers {
		if trigger.GetNamespace() == namespace || (trigger.GetKind() == "Namespace" && trigger.GetName() == namespace) {
			filtered = append(filtered, trigger)
		}
	}
	return filtered
}

func (pc *policyController) triggerMatches(
	resource unstructured.Unstructured,
	gvr schema.GroupVersionResource,
	resourceNames []string,
	excludeRules []admissionregistrationv1.NamedRuleWithOperations,
	nsSelector *metav1.LabelSelector,
) bool {
	// check if the resource matches the excluded rules
	if len(excludeRules) > 0 {
		for _, rule := range excludeRules {
			if contains(rule.APIGroups, gvr.Group) &&
				contains(rule.APIVersions, gvr.Version) &&
				contains(rule.Resources, gvr.Resource) &&
				(len(rule.ResourceNames) == 0 || contains(rule.ResourceNames, resource.GetName())) {
				return false
			}
		}
	}

	// check if the resource matches the names specified in the rule
	if len(resourceNames) > 0 {
		if !contains(resourceNames, resource.GetName()) {
			return false
		}
	}

	// check if the resource's namespace matches the namespace selector
	if nsSelector != nil {
		var labels map[string]string
		var nsName string
		// for namespace resources, check the labels directly
		if resource.GetKind() == "Namespace" {
			labels = resource.GetLabels()
			nsName = resource.GetName()
		} else {
			nsName := resource.GetNamespace()
			namespace, err := pc.client.GetResource(context.TODO(), "v1", "Namespace", "", nsName)
			if err != nil {
				pc.log.Error(err, "failed to get namespace", "name", nsName)
				return false
			}
			labels = namespace.GetLabels()
		}
		// check if the namespace labels match the namespace selector
		isMatch, err := matchutils.CheckSelector(nsSelector, labels)
		if err != nil {
			pc.log.Error(err, "failed to check namespace selector", "namespace", nsName)
			return false
		}
		if !isMatch {
			return false
		}
	}
	return true
}
