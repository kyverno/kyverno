func (pc *policyController) createURForGeneratingPolicy(gpol *policiesv1beta1.GeneratingPolicy) error {
	ur := newGenerateUR(engineapi.NewGeneratingPolicy(gpol))
	// get the downstream resources that are generated by the gpol
	downstreams := pc.watchManager.GetDownstreams(gpol.GetName())
	if len(downstreams) == 0 {
		// create a UR to restore the dynamic watcher cache in case backgound controller is restarted
		pc.log.V(4).Info("no downstream resources found for generating policy, creating UR to restore dynamic watcher cache")
		triggers := pc.getGpolTriggers(gpol.Spec.MatchConstraints)
		for _, trigger := range triggers {
			addRuleContext(ur, gpol.GetName(), common.ResourceSpecFromUnstructured(*trigger), false, true)
		}
	} else {
		// sync the gpol changes to the downstream resources
		pc.log.V(4).Info("sync gpol changes to downstream targets")
		for _, downstream := range downstreams {
			labels := downstream.GetLabels()
			// fetch the trigger resource labels from the downstream resource to build the UR
			trigger := generateutils.TriggerFromLabels(labels)
			addRuleContext(ur, gpol.GetName(), trigger, false, false)
		}
		// delete the downstream resources that are generated by the gpol
		pc.watchManager.DeleteDownstreams(gpol.GetName(), nil)
	}
	if len(ur.Spec.RuleContext) == 0 {
		return nil
	}
	pc.log.V(4).Info("creating URs for GeneratingPolicy", "total", len(ur.Spec.RuleContext))
	var errs []error
	for _, batch := range splitUR(ur, generateBatchSize) {
		if err := pc.submitUR(context.TODO(), batch); err != nil {
			errs = append(errs, err)
		}
	}
	return multierr.Combine(errs...)
}

