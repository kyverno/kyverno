package admissionpolicygenerator

import (
	"context"
	"fmt"
	"maps"
	"slices"

	policiesv1beta1 "github.com/kyverno/api/api/policies.kyverno.io/v1beta1"
	"github.com/kyverno/kyverno/pkg/admissionpolicy"
	vpolautogen "github.com/kyverno/kyverno/pkg/cel/policies/vpol/autogen"
	engineapi "github.com/kyverno/kyverno/pkg/engine/api"
	controllerutils "github.com/kyverno/kyverno/pkg/utils/controller"
	admissionregistrationv1 "k8s.io/api/admissionregistration/v1"
	apierrors "k8s.io/apimachinery/pkg/api/errors"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
)

// autogenVAPName returns the name for an autogenerated VAP derived from a ValidatingPolicy.
func autogenVAPName(polName, configKey string) string {
	return "vpol-" + polName + "-autogen-" + configKey
}

// handleAutogenVAPs creates or updates ValidatingAdmissionPolicies for each autogenerated
// pod-controller variant of the given ValidatingPolicy. This is called when the policy has
// spec.autogen.validatingAdmissionPolicy.enabled=true so that pod-controller kinds are
// governed by native VAPs rather than Kyverno admission webhooks.
func (c *controller) handleAutogenVAPs(ctx context.Context, pol *policiesv1beta1.ValidatingPolicy, exceptions []engineapi.GenericException) error {
	autogenConfigs, err := vpolautogen.Autogen(pol)
	if err != nil {
		return fmt.Errorf("failed to generate autogen configs for policy %s: %v", pol.GetName(), err)
	}

	// Track which autogen VAP names are still active so we can remove stale ones.
	activeNames := make(map[string]struct{}, len(autogenConfigs))

	for _, configKey := range slices.Sorted(maps.Keys(autogenConfigs)) {
		autogenConfig := autogenConfigs[configKey]
		vapName := autogenVAPName(pol.GetName(), configKey)
		activeNames[vapName] = struct{}{}

		if err := c.reconcileAutogenVAP(ctx, pol, vapName, autogenConfig, exceptions); err != nil {
			return err
		}
	}

	// Delete autogen VAPs whose config keys are no longer present (e.g., because the
	// autogen controller list changed).
	return c.pruneStaleAutogenVAPs(ctx, pol, activeNames)
}

// reconcileAutogenVAP creates or updates a single autogenerated VAP.
func (c *controller) reconcileAutogenVAP(
	ctx context.Context,
	pol *policiesv1beta1.ValidatingPolicy,
	vapName string,
	autogenConfig policiesv1beta1.ValidatingPolicyAutogen,
	exceptions []engineapi.GenericException,
) error {
	// Build a temporary ValidatingPolicy whose Spec is the autogenerated spec.
	// The parent policy's identity fields (name, UID) are used for owner references.
	autogenPol := &policiesv1beta1.ValidatingPolicy{
		TypeMeta:   pol.TypeMeta,
		ObjectMeta: metav1.ObjectMeta{Name: pol.GetName(), UID: pol.GetUID()},
		Spec:       *autogenConfig.Spec,
	}
	// Preserve validation actions from the parent policy.
	autogenPol.Spec.ValidationAction = pol.Spec.ValidationAction

	autogenWrapper := engineapi.NewValidatingPolicy(autogenPol)

	vapBindingName := constructBindingName(vapName)

	observedVAP, vapErr := c.getValidatingAdmissionPolicy(vapName)
	observedVAPbinding, vapBindingErr := c.getValidatingAdmissionPolicyBinding(vapBindingName)

	if vapErr != nil {
		if !apierrors.IsNotFound(vapErr) {
			return fmt.Errorf("failed to get validatingadmissionpolicy %s: %v", vapName, vapErr)
		}
		observedVAP = &admissionregistrationv1.ValidatingAdmissionPolicy{
			ObjectMeta: metav1.ObjectMeta{Name: vapName},
		}
	}
	if vapBindingErr != nil {
		if !apierrors.IsNotFound(vapBindingErr) {
			return fmt.Errorf("failed to get validatingadmissionpolicybinding %s: %v", vapBindingName, vapBindingErr)
		}
		observedVAPbinding = &admissionregistrationv1.ValidatingAdmissionPolicyBinding{
			ObjectMeta: metav1.ObjectMeta{Name: vapBindingName},
		}
	}

	if observedVAP.ResourceVersion == "" {
		if err := admissionpolicy.BuildValidatingAdmissionPolicy(c.discoveryClient, observedVAP, autogenWrapper, exceptions); err != nil {
			return fmt.Errorf("failed to build autogen validatingadmissionpolicy %s: %v", vapName, err)
		}
		if _, err := c.client.AdmissionregistrationV1().ValidatingAdmissionPolicies().Create(ctx, observedVAP, metav1.CreateOptions{}); err != nil {
			return fmt.Errorf("failed to create autogen validatingadmissionpolicy %s: %v", vapName, err)
		}
	} else {
		if _, err := controllerutils.Update(ctx, observedVAP, c.client.AdmissionregistrationV1().ValidatingAdmissionPolicies(), func(observed *admissionregistrationv1.ValidatingAdmissionPolicy) error {
			return admissionpolicy.BuildValidatingAdmissionPolicy(c.discoveryClient, observed, autogenWrapper, exceptions)
		}); err != nil {
			return fmt.Errorf("failed to update autogen validatingadmissionpolicy %s: %v", vapName, err)
		}
	}

	if observedVAPbinding.ResourceVersion == "" {
		if err := admissionpolicy.BuildValidatingAdmissionPolicyBinding(observedVAPbinding, autogenWrapper); err != nil {
			return fmt.Errorf("failed to build autogen validatingadmissionpolicybinding %s: %v", vapBindingName, err)
		}
		// Override the PolicyName to point to the autogen VAP (not "vpol-{name}").
		observedVAPbinding.Spec.PolicyName = vapName
		if _, err := c.client.AdmissionregistrationV1().ValidatingAdmissionPolicyBindings().Create(ctx, observedVAPbinding, metav1.CreateOptions{}); err != nil {
			return fmt.Errorf("failed to create autogen validatingadmissionpolicybinding %s: %v", vapBindingName, err)
		}
	} else {
		if _, err := controllerutils.Update(ctx, observedVAPbinding, c.client.AdmissionregistrationV1().ValidatingAdmissionPolicyBindings(), func(observed *admissionregistrationv1.ValidatingAdmissionPolicyBinding) error {
			if err := admissionpolicy.BuildValidatingAdmissionPolicyBinding(observed, autogenWrapper); err != nil {
				return err
			}
			observed.Spec.PolicyName = vapName
			return nil
		}); err != nil {
			return fmt.Errorf("failed to update autogen validatingadmissionpolicybinding %s: %v", vapBindingName, err)
		}
	}

	return nil
}

// deleteAutogenVAPs removes all autogenerated VAPs for the given policy.
// Called when spec.autogen.validatingAdmissionPolicy.enabled is set to false or removed.
func (c *controller) deleteAutogenVAPs(ctx context.Context, pol *policiesv1beta1.ValidatingPolicy) error {
	return c.pruneStaleAutogenVAPs(ctx, pol, nil)
}

// pruneStaleAutogenVAPs deletes autogen VAPs that are not in the activeNames set.
// Passing nil for activeNames deletes all autogen VAPs for the policy.
func (c *controller) pruneStaleAutogenVAPs(ctx context.Context, pol *policiesv1beta1.ValidatingPolicy, activeNames map[string]struct{}) error {
	// The two possible config keys are "defaults" and "cronjobs".
	for _, configKey := range []string{"defaults", "cronjobs"} {
		vapName := autogenVAPName(pol.GetName(), configKey)
		if _, active := activeNames[vapName]; active {
			continue
		}
		vapBindingName := constructBindingName(vapName)

		if _, err := c.getValidatingAdmissionPolicy(vapName); err == nil {
			if err := c.client.AdmissionregistrationV1().ValidatingAdmissionPolicies().Delete(ctx, vapName, metav1.DeleteOptions{}); err != nil && !apierrors.IsNotFound(err) {
				return fmt.Errorf("failed to delete stale autogen validatingadmissionpolicy %s: %v", vapName, err)
			}
		}
		if _, err := c.getValidatingAdmissionPolicyBinding(vapBindingName); err == nil {
			if err := c.client.AdmissionregistrationV1().ValidatingAdmissionPolicyBindings().Delete(ctx, vapBindingName, metav1.DeleteOptions{}); err != nil && !apierrors.IsNotFound(err) {
				return fmt.Errorf("failed to delete stale autogen validatingadmissionpolicybinding %s: %v", vapBindingName, err)
			}
		}
	}
	return nil
}
